            const message = result?.response?.trim() || ""; 

            const regex = /\n\s*(?=\d\.\s)/; 
            const split_recomm = message.split(regex); 
            const formatted_recomm = split_recomm.map(p => p.trim()) .filter(p => /^\d\.\s/.test(p));
